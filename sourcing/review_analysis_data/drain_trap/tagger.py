import json, re

def norm(t):
    return t.replace('\n',' ')

def tag_place(t, p):
    # 명시된 설치/사용 위치 (1개, 우선순위)
    # 세탁실/베란다/다용도실 계열
    has = lambda *ks: any(k in t for k in ks)
    sink = has('싱크대','씽크대','싱크','씽크','주방 배수','주방후드','매장 주방','하부장','씽크대 하부','싱크대 하부','싱크대하부')
    wash = has('세탁실','세탁기','세탁 배수','세탁배수','건조기','보일러','실외기')
    veranda = has('베란다','다용도실','다용도')
    toilet_floor = has('화장실 바닥','욕실 바닥','바닥 배수','바닥 하수구','바닥배수')
    shower = has('샤워부스','샤워실','욕실 샤워','샤워 하수구')
    sedae = has('세면대')
    bath = has('욕실','화장실','변기','샤워')
    kitchen = sink

    # 우선순위: 구체적 위치 → 방
    # 여러개 언급 시 대표 1개. 화장실/욕실 계열은 광범위.
    if sedae and not (bath and t.count('화장실')>1):
        # 세면대 명시
        return '세면대'
    if sink:
        # 싱크대/주방
        # 세탁실도 같이 나오면 애매 → 싱크대 우선(주방)
        return '싱크대'
    if wash or veranda:
        return '베란다다용도실'
    if shower:
        return '욕실샤워'
    if toilet_floor:
        return '화장실바닥'
    if sedae:
        return '세면대'
    if bath:
        # 화장실/욕실 일반
        return '화장실바닥' if ('바닥' in t) else '화장실바닥' if False else '욕실샤워' if False else '화장실'
    return None

# 위 함수가 usage_place 규칙 라벨과 안 맞음 → 규칙 라벨로 재매핑
# 규칙 라벨: 싱크대 / 세면대 / 화장실바닥 / 베란다다용도실 / 욕실샤워 / 주방 / 불명(미부여)
def usage_place(t):
    has = lambda *ks: any(k in t for k in ks)
    sedae = has('세면대')
    sink = has('싱크대','씽크대','싱크대하부','씽크대 하부','싱크대 하부','싱크 하수','씽크')
    kitchen = has('주방','매장 주방','주방후드','조리')
    wash_veranda = has('세탁실','세탁기','세탁 배수','세탁배수','세탁물','건조기','보일러','실외기','베란다','다용도실','다용도')
    shower = has('샤워부스','샤워실','샤워부스')
    toilet_general = has('화장실','욕실','변기')
    bath_floor = has('욕실 바닥','화장실 바닥','바닥 배수','바닥 하수구','바닥배수','바닥 하수')

    # 세면대는 특정적이므로 우선 (단 화장실 일반 언급보다)
    if sedae and not sink and not wash_veranda and not kitchen:
        return '세면대'
    if sink:
        return '싱크대'
    if kitchen and not toilet_general:
        return '주방'
    if wash_veranda:
        return '베란다다용도실'
    if shower:
        return '욕실샤워'
    if sedae:
        return '세면대'
    if bath_floor:
        return '화장실바닥'
    if toilet_general:
        return '화장실바닥'
    if kitchen:
        return '주방'
    return None

