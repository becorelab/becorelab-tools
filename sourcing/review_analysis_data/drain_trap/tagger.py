data_all=[]
for i in range(4):
    data_all.append(json.load(open(f'chunk_{i}.json')))

out=[]
for chunk in data_all:
    for r in chunk:
        t=norm(r['t'])
        rating=r['r']
        rec={'id':r['id'],'r':rating}
        rec['usage_place']=usage_place(t)
        rec['needs']=needs(t)
        if rating<=3:
            rec['pain']=pains(t)
        else:
            rec['pain']=[]
        rec['repeat']=repeat(t)
        out.append(rec)

json.dump(out, open('tagged_0.json','w'), ensure_ascii=False)
print('written', len(out))

